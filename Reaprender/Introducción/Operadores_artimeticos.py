# Los "Operadores aritméticos" son usados para hacer operaciones matematicas comunes.

a = 16
b = 3

suma = a + b # --> 19
resta = a - b # --> 13
multiplicacion = a * b # --> 48
division = a / b # --> 5.33
modulo = a % b # --> 1 (el modulo es el resto de la división)
division_int = a // b # --> 5 (redondea la división para abajo)
potencia = a ** b # 4096

# Los "Operadores relacionales" son usados para comparar 

mayor = a > b # True
menor = a < b # False
mayor_igual = a >= b # False
menor_igual = a >= b # True
igual = a == b # False
diferente = a != b # True

# Los "Operadores lógicos" son usados para tomar una decisión basada en condiciones

operador_and = a and b # Devuelve True si ambos son True
operador_or = a or b # Devuelve True si al menos 1 es True
operador_not = not a # Devuelve True si el operador es false

# Los "Operadores de asignación" son usados para asignar valores a una variable

# Ejemplo:

a += b 
a -= b
a *= b
a /= b
a %= b